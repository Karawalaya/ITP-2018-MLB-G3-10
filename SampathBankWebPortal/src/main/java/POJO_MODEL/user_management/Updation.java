package POJO_MODEL.user_management;

public class Updation {
	private String personId;
	private String addressStreet01;
	private String addressStreet02;
	private String addressCity;
	private String addressProvince;
	private int addressZIP;
	private String personalEmail;
	private String homeContact;
	private String mobileContact;
	
	public Updation() {}

	public String getPersonId() {
		return personId;
	}

	public void setPersonId(String personId) {
		this.personId = personId;
	}

	public String getAddressStreet01() {
		return addressStreet01;
	}

	public void setAddressStreet01(String addressStreet01) {
		this.addressStreet01 = addressStreet01;
	}
	public String getAddressStreet02() {
		return addressStreet02;
	}
	public void setAddressStreet02(String addressStreet02) {
		this.addressStreet02 = addressStreet02;
	}
	public String getAddressCity() {
		return addressCity;
	}
	public void setAddressCity(String addressCity) {
		this.addressCity = addressCity;
	}
	public String getAddressProvince() {
		return addressProvince;
	}
	public void setAddressProvince(String addressProvince) {
		this.addressProvince = addressProvince;
	}
	public int getAddressZIP() {
		return addressZIP;
	}
	public void setAddressZIP(int addressZIP) {
		this.addressZIP = addressZIP;
	}
	public String getPersonalEmail() {
		return personalEmail;
	}
	public void setPersonalEmail(String personalEmail) {
		this.personalEmail = personalEmail;
	}
	public String getHomeContact() {
		return homeContact;
	}
	public void setHomeContact(String homeContact) {
		this.homeContact = homeContact;
	}
	public String getMobileContact() {
		return mobileContact;
	}
	public void setMobileContact(String mobileContact) {
		this.mobileContact = mobileContact;
	}
}
